    private void btn_CadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CadastroActionPerformed
        boolean UsuarioCadastrado = false;
        if(UsuarioCadastrado){
            JOptionPane.showMessageDialog(this, "Usu치rio j치 cadastrado, deseja alterar sua senha?"); 
        }
        else{
            if(scr_Password.getText().equals(scr_ConfirmPassword.getText())){
                JOptionPane.showMessageDialog(this, "Bem-Vindo "+ txt_User.getText()+"\nSeu Cadastro foi finalizado com sucesso");
                dispose();
                tela_Menu tela_Menu = new tela_Menu();
                tela_Menu.setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(this, "As senhas devem ser id칡nticas");
            }
        }
    }//GEN-LAST:event_btn_CadastroActionPerformed

